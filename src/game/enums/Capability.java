package game.enums;

/**
 * Enum class for capabilities.
 * @author Kok Tim Ming
 */
public enum Capability {
    /**
     * Anybody that can transact.
     */
    TRANSACTABLE,
    /**
     * Anybody that can de-spawn
     */
    DESPAWNABLE,
    /**
     * Anybody that can rest on a Site of Lost Grace.
     */
    GRACE_RESTABLE,
    /**
     * Can accept remembrance
     */
    REMEMBRANCE_ACCEPTABLE
}
